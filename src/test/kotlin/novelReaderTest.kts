<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;三架军方特属直升机倏然落下，由前后两架机上飞身跃下二十名特种部队精英战士，身手干净利落，左右散开将另一架直升机护在中央；接着机门被一名特种兵拉开，由机舱内跨出一位身着棕绿色军服的英俊青年。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;此人名叫龙天羽，今年二十五岁，身高一米八三，因长期锻炼和习武练出一身结实挺拔的身材，浑身肌肉并不突出，却极具流线型的美感，给人一种充满爆发力的感觉。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽自幼在家族内习武，十七岁参军，后进入特种兵团，二十一岁时，已经成为最强特种部队‘龙腾’的王牌战士之一，随后先后参加数次对外的反恐任务，游走国外各种势力之间，令许多国外佣兵团都望而生畏。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;最成名一战，莫过于他去年，在非洲因为解救人质，与世界佣兵之王一战，拼着重伤，把对方斩杀，从而在佣兵界名声大噪，谁也想不到他竟然是华夏王牌特种兵的最年轻队长。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽重伤之后，在军区内休息了一年，刚出院就接到这次神秘的任务！
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;这时他被特种兵护送到基地进口，由六名航空基地警卫移接过去，立即带到一间设置满高科技扫描仪器的房间里，进行了身份确认与全身安全检查后，来到一扇中型铁门处，他心中纳闷：“据首长说，此次由陕西渭水一处断流的瀑布洞口处，出土的秦始皇又一座地下秘密皇陵，应与国家博物馆以及考古专门机构挂钩才对，怎么会忽然在航空基地做研究呢？”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;一名警卫伸手在铁门左边的晶莹电屏上一放，经过电脑指纹安全系统识别无误后，墙壁上凸起来一部电子密码锁仪器，警卫以快速熟练地手法输入密码，闸门随即启动，里面并非一间会议室，而是一部宽敞的升降机。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;接着龙天羽被送下百米深的地下高度机密指挥部，走出一重重闸门，放眼望去，一排全负重型武装的特种战士，负责保护安全的要职，长廊壁尽是防震防火防磁防弹的高合金的材料，并设置着热感红外线、激光扫描、射象监控、心律识别系统等高科技安全装置。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;几分钟后，龙天羽走到长廊尽头一道大型的闸门，门口正相候着一位女军官，身条纤细窈窕，柳眉樱口，皮肤白腻，配上一双清澈的双瞳，更加俏丽，可能由于在部队军区大量的训练，使她小腹间没有多余的脂肪，整个腰段柔软而有弹性，长发盘纂裹于帽内，再配合上一身绿色军装，衬出一种飒爽风姿。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;那位女军官看到龙天羽时候，心中激动万分，这可是国内王牌特种兵的队长啊，个人作战能力最强大的一个青年，在军中流传着他不败的传奇，肃然起敬，立即行个军礼，说道：“长官好！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;“你好！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;“几位首长都在里面了，龙长官请进！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽微微一笑，跨进闸门，眼前豁然明亮，竟是一个广阔的大堂。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;接着走上二楼的研究指挥室，里面仪器旁负手而立着十几位高层将领和二十多位教授以及考古人士，正隔着玻璃窗望向大堂。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;听到身后脚步声响起均蓦地回头，见龙天羽到来，一片茫然的脸上都堆起了微笑。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽见过诸位将领和教授后，对着一位五十余岁的中年询问道：“秦部长，不知道此次皇陵出土与航空领域有什么关系，为何要到此来研究呢？”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;国防副部长微微一笑：“你看那是什么？”说着，指向玻璃外大堂的中央。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽走到防弹、隔磁玻璃前，好奇望去，愕然一惊，只见场中央矗立一个七八米高、以混合金制成大熔炉似的庞然巨物，形状古怪，像科幻片里的飞行器。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;大堂内放满了各种各样的仪器，就像一艘巨型太空船的内舱，数十米余名穿着白衣男女研究人员正里外忙碌地操作各种仪器。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽望着仿佛外星宇宙飞船一般的飞行物，不解道：“这与皇陵秘密有何关系？”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;一位考古教授在旁边解说道：“一个月前在陕西渭水出土的这座地下皇陵与以往的秦始皇陵都有很大的不同之处，里面发现这个奇怪的现代科技仍无法研制出的飞行物，好像外形的飞碟！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽一头雾水，心道这怎么可能，有外星人？太荒唐了吧。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;那位考古教授继续道：“皇陵中除了淘涌淘马，战车兵器，青铜珠宝外，尚有三具石棺，雕有花纹，内陈头鬟、手镯、玉器和画像，经证实石棺的两具白骨均为女性。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;唯有一具石棺却是空的，只留有一副画像。出土之时棺盖早已被启动，猜测那具石棺中的女子当时并没有死，而是被人救走，有院士怀疑正是被外星人救出皇陵。”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽更是难以置信，好奇道：“请用幻影灯放下她的画像。”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;众人愕然，心忖：怎么他不问接下来要进行什么实验，却要看古代美女的画像了？
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;其实连龙天羽也不明白为什么好奇，当对方说到消失的女子时，他的脑海中蓦然现出一幅模糊的图象，好象自己正报着一位似曾相识的古代丽人跳出瀑布，跌落潭水的情景。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;工作人员接到到指示，经过电脑分析，在荧屏上投影出那张出土却失踪女子的画像。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽凝神望去，只见画像中的女子蛾眉敛黛，樱口欲动；秀雅芳淑，收神之间，刚强坚毅，眉梢眼角，妩媚多娇，风情万种，果然是个古代角色美女。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;忽然不禁骇然一惊，他心中却想：这般眼熟，喔，不正是这几日，自己做梦中的那个女子吗？奇怪了，她跟我有什么关系？
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;秦部长看到龙天羽惊讶神色，走过来道：“天羽啊，这件飞行器自从出土后一直将此消息封锁，博物院移交给国家科研部和航天航空部做研究，据科学院韩院长声明，这个不明飞行物极有是宇宙飞碟或移空换位器，通过足够的能量启动，可以传诵到任何位置，如果能把这种飞行物的功能研究出来，应用于军事和航空领域，国家的航天技术便能一跃千里，遥遥领先世界各国！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;“可是一些敌国的特工组织似乎得到情报，派了不少人想要过来破坏，所以我才向军部申请，把你们王牌特种兵队都调集过来，专门保护这件飞碟，不容有失！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽点头明白其中深意，他也没少干过这样的事，得知某个国家有重要情报，派去抢夺或者毁坏，同理，其它敌国知道华夏有这样的发现，肯定也会派来一些精英特工、佣兵来破坏！
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;“它被埋藏地下这么久，各方面性能会不会出现问题？”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;一名科学院叶教授说道：“放心吧，已经过专业人士多方面检修，各固件并无损坏或脱落的迹象，何况这次并不以活人做实验，只是提高能量，观测它的启动运转情况。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;旁边的叶教授兴奋不已，说道：“本来想用电力和燃气做能源，但根本达不到所需能量的百分之一，所以改用氢核聚变反应而生成的巨大能量，传输到飞碟的能量锌板上，便可以解决能源问题，负荷满载后，便可以顺利地启动；如果成功的话便定目标为三百里外的内蒙古草原军用实验区着落，那边已经做好随时接收的准备！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;便在这是，大堂中一位身穿白衣的科学人员发出一切准备就绪，等待下步指示的信号！
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;二楼内所有人员的目光都集中在国防副部长的身上。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;秦部长斩钉截铁的道：“好！各部门就位，实验立即开始！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;指令传出，实验室所有仪器立即忙碌起来，无数的指示灯亮起，动员了数百名研究员，全神操作和监察着，
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;时间漫漫过去，飞碟旁的氢核反应炉经过二十多个小时的聚变，积聚了巨大的能量，随即传到飞碟的能量锌板上。只见能量愈聚愈强，飞蝶机舱的大小指示灯霎时间全部闪起，机身不停震颤，并发出一阵刺耳的轰鸣声……
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽见状不对，急忙道：“能量已经足够，立即切断能源！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;话音刚落，主控台的机器仿佛被雷电劈中一般，火光四射，警报声忽然响起。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;负责监察炉内力磁场的研究员惊惶的声音传来：“飞碟内舱的能量板中力能不断失常地攀升，不断加强氢核反应堆的聚变，请求指示！”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;秦部长与其余人等何曾见过如此场面，不禁手心顿时冒出冷汗，不知道该如何下达命令？如果核聚变堆爆炸不但毁灭整个实验室，更会摧毁航空基地的“神舟八号载人飞船”，这队国家的损失可是难以估计的！
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽当机立断，大喊道：“关掉飞碟内舱的启动按钮，隔绝能源输入！快啊……”
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;楼下大堂中的实验员听到命令急忙冲上前去，孰知核变射出强大的气波，震碎数米内工作人员的心脉，使人前进不得。
<br />

<br />
&nbsp;&nbsp;&nbsp;&nbsp;龙天羽眼见千钧一发，当即挥拳轰出，凝聚全身之力爆发，一拳轰碎了防弹玻璃，整个人一个空翻，跃下四米多高的指挥台，疾步跨入飞碟内舱，按住一排不知什么名堂的指示钮，喝道：“赶快移空到指定目标处！”
<br />

<br />
